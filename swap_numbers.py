"""
Program: Swap Two Numbers

This program swaps the values of two variables.
"""

# --------------------------------------------------
# INPUT VALUES
# --------------------------------------------------

a = 10
b = 20

# --------------------------------------------------
# BEFORE SWAPPING
# --------------------------------------------------

print("Before swapping:")
print("a =", a)   # Output: 10
print("b =", b)   # Output: 20


# --------------------------------------------------
# SWAPPING LOGIC (WITHOUT USING TEMP VARIABLE)
# --------------------------------------------------

a, b = b, a

# --------------------------------------------------
# AFTER SWAPPING
# --------------------------------------------------

print("After swapping:")
print("a =", a)   # Output: 20
print("b =", b)   # Output: 10

# --------------------------------------------------
# END OF PROGRAM
# --------------------------------------------------
