"""
Program: Sum and Average of Two Variables

This program calculates the sum and average
of two given numbers.
"""

# --------------------------------------------------
# INPUT VALUES
# --------------------------------------------------

a = 10
b = 20

# --------------------------------------------------
# CALCULATIONS
# --------------------------------------------------

sum_result = a + b
average_result = sum_result / 2

# --------------------------------------------------
# OUTPUT
# --------------------------------------------------

print("First number:", a)          # Output: 10
print("Second number:", b)         # Output: 20
print("Sum:", sum_result)          # Output: 30
print("Average:", average_result)  # Output: 15.0

# --------------------------------------------------
# END OF PROGRAM
# --------------------------------------------------
